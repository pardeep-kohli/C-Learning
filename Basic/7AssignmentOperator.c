#include <stdio.h>
int main()
{
    int a=5;
    printf("Simple Assign Operator = %d\n",a=5);
    printf("Add and Assign Operator = %d\n",a+=5);
    printf("Subtract and Assign Operator = %d\n",a-=5);
    printf("Multiply and Assign Operator = %d\n",a*=5);
    printf("Divide and Assign Operator = %d\n",a/=5);
    return 0;
}